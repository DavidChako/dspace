CREATE TABLE IF NOT EXISTS Location (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    x0 BIGINT UNSIGNED NOT NULL,
    x1 BIGINT UNSIGNED NOT NULL,
    x2 BIGINT UNSIGNED NOT NULL,
    x3 BIGINT UNSIGNED NOT NULL
);
CREATE PRIMARY KEY on Location(id);
CREATE UNIQUE INDEX on Location(x0, x1, x2, x3)